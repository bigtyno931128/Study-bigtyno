package sparta.week02;

public class WO2 {

    public static void main(String[] args) {

        // 사칙 연산

        System.out.println(4+2);
        System.out.println(4-2);
        System.out.println(4*2);
        System.out.println(4/2);
        System.out.println(5/2);
        System.out.println(2/4);
        System.out.println(4%2);
        System.out.println(5%2);

        System.out.println("==================");

        // 우선순위 연산
        System.out.println(2 + 2 * 2 );
        System.out.println((2+2) * 2 );

    }
}
